package com.jollyclass.attence.domain;

/**
 * 员工的javabean
 * 
 * @author saber
 *
 */
public class Employee {
	/**
	 * 员工的id号
	 */
	private String id;
	/**
	 * 员工的指纹号
	 */
	private String no;
	/**
	 * 员工的名称
	 */
	private String name;
	/**
	 * 部门名称
	 */
	private String deptName;
	/**
	 * 员工考核的星期
	 */

	private String week;
	/**
	 * 员工的考勤日期
	 */
	private String attenceDate;
	/**
	 * 员工的考勤时间
	 */
	private String attenceTime;
	/**
	 * 员工的上下班标记
	 */
	private String flag;
	/**
	 * 员工的迟到早退状态
	 */
	private String status;
	/**
	 * 员工迟到时间
	 */
	private String leaveTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getAttenceDate() {
		return attenceDate;
	}

	public void setAttenceDate(String attenceDate) {
		this.attenceDate = attenceDate;
	}

	public String getAttenceTime() {
		return attenceTime;
	}

	public void setAttenceTime(String attenceTime) {
		this.attenceTime = attenceTime;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLeaveTime() {
		return leaveTime;
	}

	public void setLeaveTime(String leaveTime) {
		this.leaveTime = leaveTime;
	}

	@Override
	public String toString() {
		return "Employee [id=" + id + ", no=" + no + ", name=" + name + ", deptName=" + deptName + ", week=" + week
				+ ", attenceDate=" + attenceDate + ", attenceTime=" + attenceTime + ", flag=" + flag + ", status="
				+ status + ", leaveTime=" + leaveTime + "]";
	}


}
